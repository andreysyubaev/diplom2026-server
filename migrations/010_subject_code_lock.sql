-- Замок на код входа в предмет.
-- Когда code_locked = TRUE, текущий код перестаёт автоматически
-- истекать: при каждом обращении его expires_at продлевается, и студенты
-- могут заходить по нему сколько угодно. Полезно, например, когда препод
-- даёт ссылку на код заранее, а заходить будут через неделю.
-- При выключении лока поведение возвращается к обычному (5-минутная ротация).

ALTER TABLE subjects
    ADD COLUMN IF NOT EXISTS code_locked BOOLEAN NOT NULL DEFAULT FALSE;
